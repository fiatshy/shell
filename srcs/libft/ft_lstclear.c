#include "libft.h"

void	ft_lstclear(t_lst **lst, void (*del)(void **))
{
	t_lst	*tmp;

	if (!lst)
		return ;
	while (*lst)
	{
		tmp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = tmp;
	}
}
